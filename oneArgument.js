function numberSquared(x) {
    return x * x;
}

function numberCubed(x) {
    return numberSquared(x) * x;
}
























/////////////////////////////////////////////
//  arrow function with 1 argument  /////////
/////////////////////////////////////////////

let numberSquared =(x) => {
    return x * x;
}

/////////////////////////////////////////////
//  concise arrow function 1 argument  //////
/////////////////////////////////////////////

let numberSquared = x =>  x * x;